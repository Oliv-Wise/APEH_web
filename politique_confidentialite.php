<h1>Politique de confidentialité</h1>

<p>
    Cette politique de confidentialité vous informe sur la manière dont l’association APEH-France collecte, utilise, conserve et protège vos données personnelles, conformément au Règlement Général sur la Protection des Données (RGPD).
</p>

<h2>1. Données collectées</h2>
<p>Lors de votre inscription, nous collectons les données suivantes :</p>
<ul>
    <li>Nom, prénom</li>
    <li>Email et numéro de téléphone</li>
    <li>Adresse postale</li>
    <li>Statut (étudiant, professionnel, autre)</li>
    <li>Domaine d’activité ou d’étude</li>
    <li>Nom et téléphone d’une personne à contacter en Haïti</li>
</ul>

<h2>2. Finalité de la collecte</h2>
<p>Ces données sont utilisées uniquement dans le cadre suivant :</p>
<ul>
    <li>Organisation des activités et événements de l’association</li>
    <li>Communication avec les membres</li>
    <li>Gestion interne de l’APEH-France</li>
</ul>

<h2>3. Durée de conservation</h2>
<p>Les données sont conservées aussi longtemps que vous êtes membre actif de l’association. Vous pouvez demander leur suppression à tout moment.</p>

<h2>4. Accès aux données</h2>
<p>Seuls les responsables de l’association ont accès à vos données. Elles ne seront jamais partagées ni vendues à des tiers.</p>

<h2>5. Vos droits</h2>
<p>Conformément au RGPD, vous avez les droits suivants :</p>
<ul>
    <li>Accès à vos données personnelles</li>
    <li>Rectification ou mise à jour de vos données</li>
    <li>Demande de suppression (« droit à l’oubli »)</li>
</ul>
<p>
    Pour exercer vos droits, contactez-nous à : 
    <a href="mailto:apeh.france509@gmail.com">apeh.france509@gmail.com</a>
</p>

<h2>6. Sécurité</h2>
<p>Vos données sont stockées de manière sécurisée sur un serveur local, protégé par des identifiants, des mots de passe chiffrés et des mesures techniques (pare-feu, contrôle des accès, etc.).</p>

<h2>7. Cookies</h2>
<p>Ce site n’utilise pas de cookies à des fins de suivi ou publicitaires.</p>

