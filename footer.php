            <p class="preview">
                <strong>Statut de l’APEH-France :</strong><br>
                Selon la loi 1901, les associations sont des regroupements de personnes autour 
                d’intérêts communs. C’est sur cette base juridique que les étudiants et professionnels Haïtiens 
                de France ont décidé de se constituer en collectif.
            </p>
            <div class="full-text" style="display: none;">
                <p>
                <section class="statut-complet">

  <h2>Préambule</h2>
  <p>
    La formation à la pratique d’activités au service de l’intérêt général, l’exercice des compétences permettant de surmonter les difficultés de la vie étudiante, l’apport d’une expérience favorable à l’éclosion des qualités en complément des formations proposées par les établissements d’enseignement supérieur, la pratique de l’exercice civique et démocratique, l’ouverture à un réseau diversifié, sont les principaux éléments résumant le rôle des associations étudiantes.
  </p>
  <p>
    Conscients que leur parcours universitaire connaît, en plus des difficultés classiques des étudiants, celles liées au parcours d’étudiants étrangers, les étudiants haïtiens de Lille ont choisi de se fédérer au sein de structures et autour d’objectifs communs. Cette association œuvre pour la réussite de ses adhérents, comprenant également l’adaptation et l’intégration sociale des membres de différentes disciplines, cultures et nations.
  </p>
  <p>
    Admettons que, dans l’union et la solidarité, nous pouvons apporter notre contribution au renforcement de la coopération entre les institutions haïtiennes et françaises.
  </p>
  <p>
    La vie associative suppose un engagement bénévole, mais également un degré de responsabilité à exercer et un respect des engagements similaires à ceux du monde professionnel. En toile de fond, l’intérêt d’une responsabilisation des membres réside dans le souci de leur attribuer les outils pratiques nécessaires à leur insertion professionnelle selon les standards internationaux.
  </p>
  <p>
    Les étudiants et professionnels Haïtiens de France, portion non négligeable de la jeunesse de la République d’Haïti, considèrent le parcours d’études comme l’un des piliers fondamentaux de l’essor tant souhaité. Essor qu’ils se chargent de provoquer dès aujourd’hui.
  </p>
  <p><strong>
    Approuvons et adoptons librement et solennellement le présent statut comme texte régissant la vie et le fonctionnement de l’APEH-France.
  </strong></p>

  <h2>TITRE I – Dénomination, nature, objectifs, moyens d’action et logo</h2>

  <h3>Article 1 – Dénomination</h3>
  <p>
    Il est fondé entre les adhérents au présent statut une association régie par la loi du 1er juillet 1901 et le décret du 16 août 1901 de la République de France, dénommée <strong>Association des Professionnels et Étudiants Haïtiens de France</strong>, en abrégé <strong>APEH-FRANCE</strong>.
  </p>

  <h3>Article 2 – Nature</h3>
  <p>APEH-FRANCE est une organisation apolitique et à but non lucratif.</p>

  <h3>Article 3 – Objectifs</h3>
  <ul>
    <li>Accompagner les membres à s’intégrer</li>
    <li>Promouvoir l’interdisciplinarité</li>
    <li>Faciliter l’insertion professionnelle</li>
    <li>Valoriser la culture haïtienne</li>
    <li>Encourager les membres à mettre leurs compétences au service d’Haïti</li>
  </ul>

  <h3>Article 4 – Moyens d’action</h3>
  <ul>
    <li>Organisation de rencontres d’écoute, d’échanges et de partage d’expériences</li>
    <li>Suivi académique, tutorat, ateliers, conférences, etc.</li>
    <li>Collaboration avec les autorités administratives et universitaires</li>
    <li>Journées d’accueil, visites guidées, activités citoyennes</li>
    <li>Accompagnement dans les démarches administratives</li>
    <li>Journées culturelles et salons de l’employabilité</li>
    <li>Création d’un annuaire de diplômés</li>
    <li>Collaboration avec les autorités haïtiennes</li>
  </ul>

  <h3>Description du logo</h3>
  <ul>
    <li><strong>L’ouvrage :</strong> source de connaissances et d’imagination</li>
    <li><strong>La connaissance :</strong> compréhension approfondie des domaines d’action</li>
    <li><strong>Le savoir :</strong> ensemble des compétences acquises</li>
    <li><strong>Le partage :</strong> transmission de savoirs au bénéfice collectif</li>
    <li><strong>L’union :</strong> solidarité et cohésion pour un but commun</li>
  </ul>

  <h2>TITRE II – Durée, adhésion et membres</h2>

  <h3>Article 7 – Durée</h3>
  <p>L’APEH-FRANCE est constituée pour une durée illimitée.</p>

  <h3>Article 8 – Adhésion</h3>
  <p>
    L’adhésion est ouverte à toute personne respectant le statut et le règlement intérieur. Elle se matérialise par l’acquisition d’une carte de membre.
  </p>

  <h3>Article 9 – Membres</h3>
  <ul>
    <li><strong>Membres actifs :</strong> Haïtiens ou non, étudiants ou professionnels, à jour de cotisations</li>
    <li><strong>Membres d’honneur :</strong> Personnes rendant service à l’association ou incarnant ses valeurs</li>
    <li><strong>Sympathisants :</strong> Personnes partageant les objectifs de l’association sans y adhérer officiellement</li>
  </ul>

  <h3>Article 10 – Perte de qualité de membre</h3>
  <ul>
    <li>Démission</li>
    <li>Radiation</li>
    <li>Décès</li>
  </ul>

  <h2>TITRE III – Administration et fonctionnement</h2>

  <h3>Article 11 – Les organes</h3>
  <p>
    L’association est dirigée par une Assemblée Générale, un Bureau Exécutif, une Commission Électorale, un Commissariat aux comptes, et une Commission de Contrôle et de Surveillance.
  </p>

  <h4>Assemblée Générale (A.G.)</h4>
  <ul>
    <li>Organe suprême</li>
    <li>Réunie chaque année en janvier (AGO) ou en cas de besoin (AGE)</li>
    <li>Décide du montant des cotisations, approuve les bilans</li>
    <li>Peut se transformer en A.G élective en fin de mandat</li>
  </ul>

  <h4>Bureau Exécutif</h4>
  <p>Chargé de la gestion quotidienne, il comprend :</p>
  <ul>
    <li>Président, vice-président, secrétaire général</li>
    <li>Trésorier, trésorier adjoint</li>
    <li>Chargés de communication et de programmation</li>
  </ul>

  <h4>Élection du président</h4>
  <ul>
    <li>Élu pour un an renouvelable une fois</li>
    <li>Conditions : être haïtien, à jour de ses cotisations, avoir un an de militantisme</li>
    <li>Éligibilité confirmée par une cotisation spéciale de 50€</li>
    <li>Vote direct ou par procuration</li>
  </ul>

  <h4>Commissariat aux comptes</h4>
  <p>Contrôle les finances et est désigné dans les mêmes conditions que le président.</p>

  <h4>Commission de Contrôle et de Surveillance</h4>
  <p>Assure la régularité des activités et résout les différends.</p>

  <h4>Commission électorale</h4>
  <ul>
    <li>Statue sur les candidatures</li>
    <li>Organise et supervise les élections</li>
  </ul>

  <h3>Article 12 – Ressources</h3>
  <ul>
    <li>Droits d’entrée et cotisations</li>
    <li>Subventions publiques</li>
    <li>Toutes ressources légales</li>
  </ul>

  <h3>Article 13 – Gratuité des fonctions</h3>
  <p>Toutes les fonctions sont bénévoles ; les frais sont remboursés sur justificatif.</p>

  <h3>Article 14 – Règlement intérieur</h3>
  <p>
    Un règlement intérieur fixera les détails non prévus dans le présent statut.
  </p>

  <h2>TITRE IV – Dissolution</h2>

  <h3>Article 15 – Procédure</h3>
  <p>Décidée en A.G ordinaire à la majorité des 2/3.</p>

  <h3>Article 16 – Liquidation</h3>
  <p>
    Désignation d’un ou plusieurs liquidateurs ; dévolution de l’actif net à un organisme ou membre conformément à la décision de l’A.G.
  </p>

  <h2>TITRE V – Dispositions transitoires</h2>

  <h3>Article 17 – Disposition finale</h3>
  <p>
    Le présent statut prend effet après son adoption par l’Assemblée Générale.
    <br><br>
    <strong>Adopté à Villeneuve d’Ascq, le 01/01/2023</strong><br>
    <em>Signé par le Président : Steeve MICHEL</em><br>
    <em>et le Secrétaire Général : Marcy LEON</em>
  </p>

