        </ul>

        <h3>Commissariat aux comptes</h3>
        <p>Contrôle les finances</p>

        <h3>Commission de Contrôle</h3>
        <p>Vérifie la régularité des activités</p>

        <h3>Commission électorale</h3>
        <p>Gère les candidatures et élections</p>

        <h3>Article 12 – Ressources</h3>
        <ul>
            <li>Cotisations</li>
            <li>Subventions</li>
            <li>Autres ressources légales</li>
        </ul>

        <h3>Article 13 – Gratuité des fonctions</h3>
        <p>Les fonctions sont bénévoles.</p>

        <h3>Article 14 – Règlement intérieur</h3>
        <p>Complète le statut sur les détails pratiques.</p>

        <h2>TITRE IV – Dissolution</h2>
